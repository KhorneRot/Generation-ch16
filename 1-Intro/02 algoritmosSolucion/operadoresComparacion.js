// igualdad (es recomendado usar el igual estricto "===")
console.log(5 == 5);
console.log(1 == 3);

console.log(5 == "5"); // caso especial

console.log("hola" == "adios");
console.log("hola" == "hola ");

console.log(false == true);

// desigualdad

console.log(2 != 5);
console.log("gato" != "gato");
console.log("gato" != 1);

// mayor
console.log(10 > 5);
console.log(10 > 20);
console.log(5 > 5);

// mayor o igual

console.log(5 >= 5);
console.log(10 >= 3);
console.log(3 >= 7);

// menor
console.log(10 < 20);
console.log(20 < 10);

// menor o igual

console.log(10 <= 10);
console.log(10 <= 15);
console.log(10 <= 5);

// numeros (caso muy raro)

console.log("a" < "b");
console.log("c" < "b");
